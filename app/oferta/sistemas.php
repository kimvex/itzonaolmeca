    <article class="carrera-def">
      <h3>Definición</h3>
      <p class="definicion">La ingeniería en sistemas computacionales o ingeniería informática es un modo de enfoque interdisciplinario que permite estudiar y comprender la realidad, con el propósito de implementar u optimizar sistemas informáticos complejos.</p>
      <h3>Objetivo General</h3>
      <p class="definicion">Formar profesionistas líderes, analíticos, críticos y creativos, con visión estratégica y amplio sentido ético, capaces de diseñar, implementar y administrar infraestructura computacional para aportar soluciones innovadoras en beneficio de la sociedad, en un contexto global, multidisciplinario y sustentable.</p>
    </article>
