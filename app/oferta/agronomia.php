      <h3>Ingeniería en Agronomía</h3>
    </article>
    <article class="carrera-def">
      <h3>Definición</h3>
      <p class="definicion">Es el conjunto de conocimientos de diversas ciencias aplicadas que rigen la práctica de la agricultura.</p>
      <h3>Objetivo General</h3>
      <p class="definicion">Formar profesionistas en el campo de la agronomía capaces de generar, adaptar, transferir tecnología y desarrollar procesos de producción agropecuaria, con base en estándares de calidad, vocación de servicio, visión creativa y emprendedora, comprometidos con la sociedad y el manejo sustentable de los recursos naturales.</p>
    </article>
    <span>
      <h3>Plan de Estudios</h3>
    </span>
    <embed class="pdf-carreras" src="../../assets/pdf/Reticula Ingenieria en Agronomia IAGR-2010-214.pdf" width="800px" height="500px" type="application/pdf">
    <article class="carrera-contenido">
      <div class="opciones">
        <h3>Perfil de Ingreso</h3>
        <p>
          El aspirante a cursar una ingeniería debe contar de preferentemente con:

          1.  Aptitudes: en el cálculo científico, capacidad de análisis y síntesis, en el manejo de conceptos abstractos, organización, resistencia física, inventiva y originalidad, sociabilidad, de comunicación, dibujo y visión de conjunto, habilidad en el trato con la gente.
          2.  Actitudes: estabilidad emocional, gusto por las actividades al aire libre, sentido de autoridad, respeto de valores, costumbres y tradiciones.

          Además debe haber concluido el Bachillerato en el Área de las Ciencias Físico-Matemáticas y de las Ingenierías, y tener una buena preparación en Matemáticas, Física y Química, puesto que el estudio de la carrera requiere una sólida base en estas ciencias. 
          Asimismo, necesita poseer:

          • Conocimientos básicos de un idioma extranjero; Inglés preferentemente. Además del manejo de la Computación.
          • Creatividad y destreza para analizar y resolver problemas.
          • Capacidad para la toma de decisiones; para observar los fenómenos físicos y lograr su interpretación.
          • Facilidad de adaptación a sesiones de trabajo prolongadas, bajo condiciones y ambientes físicos adversos.
          • Aptitud para tratar personas con diferente preparación y criterio.
          • Habilidad para organizar y dirigir grupos de trabajo.
          • Buen manejo de la comunicación oral y escrita.
          • Actitud de compromiso al trabajo, con disciplina y orden.
          • Interés en la cultura y en todos los campos del conocimiento.
          • Inclinación por la investigación.
        </p>
        <h3>Perfil de Egreso</h3>
        <p class="margen-final">1. Desarrollar procesos productivos agropecuarios con un enfoque sustentable.
        
        2. Desempeñar el ejercicio de su profesión sobre bases científico-tecnológicas, participando en equipos inter y multidisciplinarios en sistemas de producción agropecuaria.
        
        3. Identificar y analizar la problemática social, económica y política, en los ámbitos regional, nacional e internacional para la toma de decisiones en el desempeño de su profesión.
        
        4. Manejar las tecnologías de información y comunicación para obtener, procesar y compartir información en la construcción del conocimiento aplicado a los sistemas de producción de bienes y servicios.
        
        5. Generar, transferir y aplicar alternativas tecnológicas de aprovechamiento sustentable de los recursos naturales para el mejoramiento de los agroecosistemas.
        
        6. Integrar a los productores agropecuarios en las cadenas productivas en la transformación de productos para generar valor agregado.
        
        7. Identificar riesgos e implementar estrategias de bioseguridad e inocuidad en los sistemas productivos agropecuarios.
        
        8. Ejercer liderazgo en las áreas de su desempeño profesional para propiciar e impulsar cambios que mejoren las condiciones de vida de la sociedad.
        
        9. Identificar, gestionar y aplicar los programas; así como, esquemas de apoyo técnico, financiero, social y de comercialización para el desarrollo regional.
        
        10. Desarrollar habilidades de comunicación verbal y escrita, interpretando correctamente información pertinente en su propio idioma y en una segunda lengua.
        
        11. Actuar y conducirse con principios éticos en el ejercicio de la profesión.
        
        12. Utilizar estratégicamente los principios socioeconómicos, administrativos, legislativos y normativos inherentes al ejercicio de su profesión.</p>
