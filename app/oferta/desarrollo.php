      <h3>Ingeniería en Desarrollo Comunitario</h3>
    </article>
    <article class="carrera-def">
      <h3>Definición</h3>
      <p class="definicion">El Desarrollo Rural Comunitario abarca un rango de enfoques y actividades cuyo principal objetivo es la mejora del bienestar y el sustento de las personas, estas aproximaciones atienden a asuntos sociales, particularmente a la organización de la comunidad.</p>
      <h3>Objetivo General</h3>
      <p class="definicion">Formar ingenieros en desarrollo comunitario basados en competencias profesionales para la planeación, gestión, ejecución y evaluación de proyectos comunitarios, promoviendo el diálogo intercultural entre la compleja diversidad de las relaciones sociales, políticas y culturales, favoreciendo los procesos de vinculación comunitaria y el manejo sustentable de los recursos naturales.</p>
    </article>
    <span>
      <h3>Plan de Estudios</h3>
    </span>
    <embed class="pdf-carreras" src="../../assets/pdf/Reticula Ingenieria en Desarrollo Comunitario IDCO-2010-216.pdf" width="800px" height="500px" type="application/pdf">
    <article class="carrera-contenido">
      <div class="opciones">
        <h3>Perfil de Ingreso</h3>
        <p>
          El aspirante a cursar una ingeniería debe contar de preferentemente con:

          1.  Aptitudes: en el cálculo científico, capacidad de análisis y síntesis, en el manejo de conceptos abstractos, organización, resistencia física, inventiva y originalidad, sociabilidad, de comunicación, dibujo y visión de conjunto, habilidad en el trato con la gente.
          2.  Actitudes: estabilidad emocional, gusto por las actividades al aire libre, sentido de autoridad, respeto de valores, costumbres y tradiciones.

          Además debe haber concluido el Bachillerato en el Área de las Ciencias Físico-Matemáticas y de las Ingenierías, y tener una buena preparación en Matemáticas, Física y Química, puesto que el estudio de la carrera requiere una sólida base en estas ciencias. 
          Asimismo, necesita poseer:

          • Conocimientos básicos de un idioma extranjero; Inglés preferentemente. Además del manejo de la Computación.
          • Creatividad y destreza para analizar y resolver problemas.
          • Capacidad para la toma de decisiones; para observar los fenómenos físicos y lograr su interpretación.
          • Facilidad de adaptación a sesiones de trabajo prolongadas, bajo condiciones y ambientes físicos adversos.
          • Aptitud para tratar personas con diferente preparación y criterio.
          • Habilidad para organizar y dirigir grupos de trabajo.
          • Buen manejo de la comunicación oral y escrita.
          • Actitud de compromiso al trabajo, con disciplina y orden.
          • Interés en la cultura y en todos los campos del conocimiento.
          • Inclinación por la investigación.
        </p>
        <h3>Perfil de Egreso</h3>
        <p class="margen-final">1. Ser capaz de organizar y crear grupos de trabajo para el desarrollo integral de la comunidad.
        
        2. Realizar diagnósticos comunitarios, empleando metodologías que permitan detectar y priorizar necesidades en los aspectos económico, social, productivo‐tecnológico y ambiental en las comunidades.
        
        3. Identificar y evaluar la integración de sistemas y cadenas productivas eficientes que impliquen manejo y transformación de productos, bajo normas de calidad nacional e internacional.
        
        4. Diseñar planes de desarrollo mediante metodologías participativas que resuelvan de manera holística las demandas de la sociedad en diversos esquemas situacionales para la mejora de la comunidad‐región.
        
        5. Gestionar recursos mediante el acompañamiento y presentación de proyectos a diferentes instancias gubernamentales y no gubernamentales.
        
        6. Implementar proyectos de desarrollo social y tecnológico que atienda a criterios de innovación, sustentabilidad, legalidad y lo relativo a los usos y costumbres, con la finalidad de promover el desarrollo de la comunidad.
        
        7. Aplicar Tecnología mediante un enfoque sustentable para la solución de problemas sociales, ambientales y productivos.
        
        8. Fortalecer el capital social a través del conocimiento y la revaloración de la cultura de la vida comunitaria que facilite el desarrollo.
        
        9. Vincular los aportes de la ciencia y la tecnología con los procesos de administración de los recursos naturales y con las actividades productivas, respetando el medio ambiente de la región.</p>
