      <h3>Ingeniería Forestal</h3>
    </article>
    <article class="carrera-def">
      <h3>Definición</h3>
      <p class="definicion">Profesión que abarca la ciencia, el arte y la práctica de crear, administrar, usar y preservar los bosques y recursos asociados para beneficio humano y de manera sustentable a fin de satisfacer las metas, necesidades y valores deseados.</p>
      <h3>Objetivo General</h3>
      <p class="definicion">Formar profesionales con capacidad técnica, analítica, crítica, reflexiva, científica, tecnológica y emprendedora, para el manejo sustentable de los ecosistemas forestales en beneficio de la sociedad.</p>
    </article>
    <span>
      <h3>Plan de Estudios</h3>
    </span>
    <embed class="pdf-carreras" src="../../assets/pdf/Reticula Ingenieria Forestal IFOR-2010-226.pdf" width="800px" height="500px" type="application/pdf">
    <article class="carrera-contenido">
      <div class="opciones">
        <h3>Perfil de Ingreso</h3>
        <p>
          El aspirante a cursar una ingeniería debe contar de preferentemente con:

          1.  Aptitudes: en el cálculo científico, capacidad de análisis y síntesis, en el manejo de conceptos abstractos, organización, resistencia física, inventiva y originalidad, sociabilidad, de comunicación, dibujo y visión de conjunto, habilidad en el trato con la gente.
          2.  Actitudes: estabilidad emocional, gusto por las actividades al aire libre, sentido de autoridad, respeto de valores, costumbres y tradiciones.

          Además debe haber concluido el Bachillerato en el Área de las Ciencias Físico-Matemáticas y de las Ingenierías, y tener una buena preparación en Matemáticas, Física y Química, puesto que el estudio de la carrera requiere una sólida base en estas ciencias. 
          Asimismo, necesita poseer:

          • Conocimientos básicos de un idioma extranjero; Inglés preferentemente. Además del manejo de la Computación.
          • Creatividad y destreza para analizar y resolver problemas.
          • Capacidad para la toma de decisiones; para observar los fenómenos físicos y lograr su interpretación.
          • Facilidad de adaptación a sesiones de trabajo prolongadas, bajo condiciones y ambientes físicos adversos.
          • Aptitud para tratar personas con diferente preparación y criterio.
          • Habilidad para organizar y dirigir grupos de trabajo.
          • Buen manejo de la comunicación oral y escrita.
          • Actitud de compromiso al trabajo, con disciplina y orden.
          • Interés en la cultura y en todos los campos del conocimiento.
          • Inclinación por la investigación.
        </p>
        <h3>Perfil de Egreso</h3>
        <p class="margen-final">1. Elaborar, ejecutar y evaluar programas de manejo sustentable de los ecosistemas forestales.
        
        2. Aplicar y adaptar correctamente las metodologías de evaluación de los recursos forestales maderables y no maderables.
        
        3. Generar investigación y transferencia de tecnología apropiada para impulsar el crecimiento de la producción, productividad y competitividad del área forestal.
        
        4. Planear y ejecutar proyectos sobre manejo sustentable de cuencas.
        
        5. Elaborar e implementar estudios de impacto ambiental y estudios técnicos justificativos en los diversos ecosistemas.
        
        6. Elaborar e implementar proyectos de conservación de áreas naturales y restauración de áreas degradadas.
        
        7. Organizar, capacitar y asesorar a los actores del sector forestal, para poner en marcha proyectos productivos que impulsen el desarrollo.
        
        8. Sensibilizar a la sociedad sobre el valor de los ecosistemas forestales para su conservación, protección y restauración.
        
        9. Establecer estrategias de diversificación productiva aplicando la ingeniería de proyectos a los sistemas de producción forestal, para desarrollar cadenas productivas que generen valor agregado.
        
        10. Conocer e interpretar las políticas, normas y leyes que regulan la actividad forestal.
        
        11. Usar y manejar la tecnología apropiada para la transformación de los recursos forestales.
        
        12. Utilizar las tecnologías de información y comunicación, aplicadas al manejo de los ecosistemas forestales.
        
        13. Desarrollar la habilidad de gestión ante instancias locales, nacionales e internacionales.
        
        14. Formular y aplicar los criterios e indicadores para la certificación de los procesos de aprovechamiento de los recursos forestales.
        
        15. Identificar, diagnosticar, prevenir y controlar los factores de deterioro de los ecosistemas forestales.
        
        16. Coordinar y colaborar con equipos multidisciplinarios e interinstitucionales.</p>
